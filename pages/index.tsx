import Head from "next/head";
import {
  Button,
  TextField,
  List,
  ListItem,
  IconButton,
  Box,
  CircularProgress,
  Container,
  Typography,
  Paper,
  InputBase,
} from "@mui/material";
import { useState } from "react";
import DetailsModal from "../components/DetailsModal";
import SearchIcon from "@mui/icons-material/Search";

export interface BreweryData {
  id: string;
  name: string;
  brewery_type: string;
  street: string;
  state: string;
  city: string;
  postal_code: string;
  latitude: string;
  longitude: string;
  website_url: string;
}

export default function Home() {
  const [loading, setLoading] = useState(false);
  const [input, setInput] = useState("");
  const [selectedBrewery, setSelectedBrewery] = useState<BreweryData | null>(
    null
  );
  const [breweries, setBreweries] = useState([]);
  const handleOpen = (brewery: BreweryData) => () => {
    setSelectedBrewery((selectedCampaign) =>
      selectedBrewery ? null : brewery
    );
  };

  const handleClose = () => {
    setSelectedBrewery(null);
  };

  const callAPI = async () => {
    setLoading(true);
    try {
      const res = await fetch(
        `https://api.openbrewerydb.org/breweries/search?query=${input}`
      );
      const data = await res.json();
      setBreweries(data);
      console.log(data);
    } catch (err) {
      console.log(err);
    } finally {
      setLoading(false);
    }
  };

  return (
    <div>
      <Head>
        <title>Brewery Locator</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Container>
        <Box
          display="flex"
          alignItems="center"
          flexDirection="column"
          mt={6}
          mb={3}
        >
          <Typography variant="h3" component="h1" fontWeight={600}>
            Brewery Locator
          </Typography>
        </Box>
        <Box display="flex" alignItems="center" flexDirection="column">
          <Paper
            component="form"
            sx={{
              p: "2px 4px",
              display: "flex",
              alignItems: "center",
              width: 400,
            }}
            onSubmit={(e) => {
              e.preventDefault();
              callAPI();
            }}
          >
            <InputBase
              sx={{ ml: 1, flex: 1 }}
              placeholder="Search for breweries by keyword"
              inputProps={{ "aria-label": "Search for breweries by keyword" }}
              onChange={(e) => setInput(e.target.value)}
            />
            <IconButton aria-label="search" type="button">
              <SearchIcon />
            </IconButton>
          </Paper>
        </Box>
        <div>
          {loading && (
            <Container>
              <CircularProgress />
            </Container>
          )}
          {breweries.map((brewery: BreweryData) => (
            <List key={brewery.id}>
              <ListItem>
                <div>{brewery.name}</div>
                <div>
                  {brewery.street} <br /> {brewery.city}, {brewery.state}{" "}
                  {brewery.postal_code}
                </div>
                <div>{brewery.brewery_type}</div>
                <Button variant="outlined" onClick={handleOpen(brewery)}>
                  View Details
                </Button>
                <DetailsModal
                  open={selectedBrewery?.id === brewery.id}
                  brewery={brewery}
                  onClose={handleClose}
                />
              </ListItem>
            </List>
          ))}
        </div>
      </Container>
    </div>
  );
}
