import Head from "next/head";
import {
  List,
  Box,
  Container,
  Typography,
  Paper,
  InputBase,
  Link,
} from "@mui/material";
import { useState } from "react";
import SearchIcon from "@mui/icons-material/Search";
import { LoadingButton } from "@mui/lab";
import { AcUnit } from "@mui/icons-material";
import BreweryListItem from "../components/BreweryListItem";

export interface BreweryData {
  id: string;
  name: string;
  brewery_type: string;
  street: string;
  state: string;
  city: string;
  postal_code: string;
  latitude: string;
  longitude: string;
  website_url: string;
}

export default function Home() {
  const [loading, setLoading] = useState(false);
  const [input, setInput] = useState("");
  const [breweries, setBreweries] = useState([]);

  const callAPI = async () => {
    setLoading(true);
    try {
      const byKeywordResponse = await fetch(
        `https://api.openbrewerydb.org/breweries/search?query=${input}`
      );
      const byKeywordData = await byKeywordResponse.json();
      setBreweries(byKeywordData);
    } catch (err) {
      console.log(err);
    } finally {
      setLoading(false);
    }
  };

  let numberOfBreweries = Object.keys(breweries).length;

  return (
    <Box>
      <Head>
        <title>Brewery Locator</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Container>
        <Box
          display="flex"
          alignItems="center"
          flexDirection="column"
          pt={4}
          pb={3}
        >
          <Typography variant="h4" component="h1" fontWeight={600} pb={2}>
            Brewery Locator
          </Typography>
          <Typography variant="body1">
            Made with <AcUnit /> in Minneapolis by{" "}
            <Link
              href={"https://github.com/wil-gerard"}
              target="_blank"
              rel="noopener noreferrer"
            >
              Wil Gerard
            </Link>
          </Typography>
        </Box>
        <Box display="flex" alignItems="center" flexDirection="column">
          <Paper
            component="form"
            sx={{
              p: "2px 4px",
              display: "flex",
              alignItems: "center",
              width: 300,
            }}
            onSubmit={(e) => {
              e.preventDefault();
              callAPI();
            }}
          >
            <InputBase
              sx={{ ml: 1, flex: 1 }}
              placeholder="Search breweries by keyword"
              inputProps={{ "aria-label": "Search for breweries by keyword" }}
              onChange={(e) => setInput(e.target.value)}
            />
            <LoadingButton aria-label="search" type="submit" loading={loading}>
              <SearchIcon />
            </LoadingButton>
          </Paper>
        </Box>
        <Paper
          sx={{
            overflowY: "scroll",
            overflowX: "hidden",
            height: "65vh",
            marginTop: "2vh",
          }}
        >
          <List>
            {breweries.map((brewery: BreweryData) => (
              <BreweryListItem key={brewery.id} brewery={brewery} />
            ))}
          </List>
        </Paper>
        {numberOfBreweries > 0 && (
          <Box paddingTop="8px">
            <Typography>Displaying {numberOfBreweries} results</Typography>
          </Box>
        )}
      </Container>
    </Box>
  );
}
